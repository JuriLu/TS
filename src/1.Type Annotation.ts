`Declaring the type to a const is called 'type annotation' `

// STRING
let movieTitle: string = "Amadeus";
movieTitle = "Arrival";
// movieTitle = 9   

//NUMBER
let numcatLives: number = 9;
numcatLives += 1;
// numcatLives = 'zero'  

//BOOLEAN
let gameOver: boolean = false;
gameOver = true;
// gameOver = 'true';   
